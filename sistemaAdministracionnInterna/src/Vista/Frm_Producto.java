    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        String IdProducto = txtProducto.getText();
        Producto p = dp.buscar(IdProducto);
        if (p == null) {
            JOptionPane.showMessageDialog(null, "El id del producto es incorrecto");
        } else {
            dp.borrar(p.getIdProducto());
            JOptionPane.showMessageDialog(null, "El id del producto fue eliminado correctamente");
            muestra();
        }
        limpiar();
    }//GEN-LAST:event_btneliminarActionPerformed

