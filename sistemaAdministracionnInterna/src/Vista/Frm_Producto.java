    void muestra() {
        Producto_controlador pc = new Producto_controlador();
        DefaultTableModel dt = (DefaultTableModel) tblProductos.getModel();
        dt.setRowCount(0);
        for (Producto p : pc.obtener()) {
            Object v[] = {p.getIdProducto(), p.getIdMaterial(), p.getNombreProducto(), p.getStockProductoTerminado(), p.getPrecioUnitario()};
            dt.addRow(v);
        }
    }
    
