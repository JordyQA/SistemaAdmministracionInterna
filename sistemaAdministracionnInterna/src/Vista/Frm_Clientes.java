    void agregar() {
        String IdCliente = txtIdCliente.getText();
        String NombreCliente = txtNombreCliente.getText();
        String DniCliente = txtDniCliente.getText();
        String DireccionCliente = txtDireccionCliente.getText();
        String TelefonoCliente = txtTelefonoCliente.getText();
        String CorreoCliente = txtCorreoCliente.getText();

        Object[] ob = new Object[6];
        ob[0] = IdCliente;
        ob[1] = NombreCliente;
        ob[2] = DniCliente;
        ob[3] = DireccionCliente;
        ob[4] = TelefonoCliente;
        ob[5] = CorreoCliente;
        dao.add(ob);
    }

    void eliminar() {
        int fila = jtblDatos.getSelectedRow();
        String IdCliente = jtblDatos.getValueAt(fila, 0).toString();

        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "No se seleccion√≥ ninguna fila");

        } else {
            dao.eliminar(IdCliente);

        }
    }

    void limpiarTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

    void limpiarEntradas() {
        txtIdCliente.setText("");
        txtNombreCliente.setText("");
        txtDniCliente.setText("");
        txtDireccionCliente.setText("");
        txtTelefonoCliente.setText("");
        txtCorreoCliente.setText("");
        txtIdCliente.requestFocus();
    }

