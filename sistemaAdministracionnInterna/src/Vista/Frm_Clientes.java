    void listar() {
        List<Cliente> lista = dao.listar();
        modelo = (DefaultTableModel) jtblDatos.getModel();
        Object[] ob = new Object[6];

        for (int i = 0; i < lista.size(); i++) {
            ob[0] = lista.get(i).getIdCliente();
            ob[1] = lista.get(i).getNombreCliente();
            ob[2] = lista.get(i).getDniCliente();
            ob[3] = lista.get(i).getDireccionCliente();
            ob[4] = lista.get(i).getTelefonoCliente();
            ob[5] = lista.get(i).getCorreoCliente();
            modelo.addRow(ob);
        }
        jtblDatos.setModel(modelo);
    }

