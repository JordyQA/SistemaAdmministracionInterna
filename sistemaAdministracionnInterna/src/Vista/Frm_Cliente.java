    private void jtblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblDatosMouseClicked

        int fila = jtblDatos.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "No se seleccionó ninguna fila");

        } else {

            String IdCliente = jtblDatos.getValueAt(fila, 0).toString();
            String NombreCliente = jtblDatos.getValueAt(fila, 1).toString();
            String DniCliente = jtblDatos.getValueAt(fila, 2).toString();
            String DireccionCliente = jtblDatos.getValueAt(fila, 3).toString();
            String TelefonoCliente = jtblDatos.getValueAt(fila, 4).toString();
            String CorreoCliente = jtblDatos.getValueAt(fila, 5).toString();

            txtIdCliente.setText(IdCliente);
            txtNombreCliente.setText(NombreCliente);
            txtDniCliente.setText(DniCliente);
            txtDireccionCliente.setText(DireccionCliente);
            txtTelefonoCliente.setText(TelefonoCliente);
            txtCorreoCliente.setText(CorreoCliente);

        }

    }//GEN-LAST:event_jtblDatosMouseClicked

