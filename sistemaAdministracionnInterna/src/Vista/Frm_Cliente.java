    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String IdCliente = txtIdCliente.getText();
        String NombreCliente = txtNombreCliente.getText();
        String DniCliente = txtDniCliente.getText();
        String DireccionCliente = txtDireccionCliente.getText();
        String TelefonoCliente = txtTelefonoCliente.getText();
        String CorreoCliente = txtCorreoCliente.getText();

        Cliente c = new Cliente(IdCliente, NombreCliente, DniCliente, DireccionCliente, TelefonoCliente, CorreoCliente);
        dao.modificar(c);
        JOptionPane.showMessageDialog(null, "Datos del cliente modificados");
        limpiarTabla();
        listar();
        limpiarEntradas();
        
    }//GEN-LAST:event_btnModificarActionPerformed

