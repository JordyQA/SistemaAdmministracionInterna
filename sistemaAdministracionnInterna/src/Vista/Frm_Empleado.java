    void mostrar() {
        DefaultTableModel dt = (DefaultTableModel) tabla.getModel();
        dt.setRowCount(0);
        for (Empleado e : de.obtener()) {
            Object v[] = {e.getIdEmpleado(), e.getNombreEmpleado(), e.getDniEmpleado(), e.getDireccionEmpleado(), e.getTelefonoEmpleado(), e.getEspecializacion(), e.getEdad(), e.getTiempoContrato()};
            dt.addRow(v);
        }
    }
    
