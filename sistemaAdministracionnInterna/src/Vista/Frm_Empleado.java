    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        String IdEmpleado, NombreEmpleado, DniEmpleado, DireccionEmpleado, TelefonoEmpleado, Especializacion, TiempoContrato;
        int Edad;

        IdEmpleado = txtIdEmpleado.getText();
        NombreEmpleado = txtNombreEmpleado.getText();
        DniEmpleado = txtDniEmpleado.getText();
        DireccionEmpleado = txtDireccionEmpleado.getText();
        TelefonoEmpleado = txtTelefonoEmpleado.getText();
        Especializacion = txtEspecializacion.getText();
        Edad = Integer.parseInt(txtEdad.getText());
        TiempoContrato = txtTiempoContrato.getText();
        Empleado e = new Empleado(IdEmpleado, NombreEmpleado, DniEmpleado, DireccionEmpleado, TelefonoEmpleado, Especializacion, Edad, TiempoContrato);
        de.crear(e);
        JOptionPane.showMessageDialog(null, "Empleado guardado con éxito");
        mostrar();
        limpiar();
    }//GEN-LAST:event_btnguardarActionPerformed

