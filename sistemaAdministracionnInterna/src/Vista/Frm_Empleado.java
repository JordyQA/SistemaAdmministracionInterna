    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        String IdEmpleado, NombreEmpleado, DniEmpleado, DireccionEmpleado, TelefonoEmpleado, Especializacion, TiempoContrato;
        int Edad;

        IdEmpleado = txtIdEmpleado.getText();
        NombreEmpleado = txtNombreEmpleado.getText();
        DniEmpleado = txtDniEmpleado.getText();
        DireccionEmpleado = txtDireccionEmpleado.getText();
        TelefonoEmpleado = txtTelefonoEmpleado.getText();
        Especializacion = txtEspecializacion.getText();
        Edad = Integer.parseInt(txtEdad.getText());
        TiempoContrato = txtTiempoContrato.getText();

        Empleado e = new Empleado(IdEmpleado, NombreEmpleado, DniEmpleado, DireccionEmpleado, TelefonoEmpleado, Especializacion, Edad, TiempoContrato);
        de.actualizar(e);
        JOptionPane.showMessageDialog(null, "Empleado modificado con éxito");
        mostrar();
        limpiar();
    }//GEN-LAST:event_btnmodificarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        String IdEmpleado = txtIdEmpleado.getText();
        Empleado e = de.buscar(IdEmpleado);
        if (e == null) {
            JOptionPane.showMessageDialog(null, "El id del empleado es incorrecto");
        } else {
            de.borrar(e.getIdEmpleado());
            JOptionPane.showMessageDialog(null, "El id del empleado fue eliminado correctamente");
            mostrar();
        }
        limpiar();
    }//GEN-LAST:event_btneliminarActionPerformed

