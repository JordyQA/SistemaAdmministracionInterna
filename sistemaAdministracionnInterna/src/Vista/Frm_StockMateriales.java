    public void cargarTabla() {
        String sql = "";
        Conexion cn = new Conexion();
        Connection c = cn.getConexion();
        modelo = new DefaultTableModel(null, cabecera);
        sql = "select * from almacen";
        try {
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString("IdMaterial");
                datos[1] = rs.getString("TipoMaterial");
                datos[2] = rs.getString("StockMaterial");
                datos[3] = rs.getString("PrecioUnidad");
                datos[4] = rs.getString("categoria");
                datos[5] = rs.getString("fechaRegistro");

                modelo.addRow(datos);
            }
            tblProductos.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error " + e);
        }
    }

