    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        //validamos que el campo codigo texto este lleno
        if (txtCodigo.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese el codigo");
            txtCodigo.requestFocus();
            return;
        }
        //validamos que el campo Descripcion texto este lleno
        if (txtDescripcion.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese la descripción");
            txtDescripcion.requestFocus();
            return;
        }
        //validamos que el campo cantidad texto este lleno
        if (txtCantidad.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese la cantidad");
            txtCantidad.requestFocus();
            return;
        }
        //validamos que el campo precio texto este lleno
        if (txtPrecio.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese el precio");
            txtPrecio.requestFocus();
            return;
        }
        //validamos que el campo categoria este lleno
        if (txtCategoria.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese la categoria");
            txtCategoria.requestFocus();
            return;
        }

        stock.setCodigo(txtCodigo.getText());
        stock.setDescripcion(txtDescripcion.getText());
        stock.setCantidad(Integer.parseInt(txtCantidad.getText()));
        stock.setPrecio(Float.parseFloat(txtPrecio.getText()));
        stock.setCategoria(txtCategoria.getText());

        //para poder ingresar la fecha del calendario
        Calendar cal;
        int d, m, a;
        cal = feRegistro.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        stock.setFechaRegistro(new Date(a, m, d));

        //validamos si el registro se ingreso correctamnte
        if (sc.insertar(stock)) {
            JOptionPane.showMessageDialog(null, "Registro insertado correctamente");
        } else {
            JOptionPane.showMessageDialog(null, "Error: registro no pudo ser insertado");
        }

        limpiar();
        cargarTabla();
        inabilitar();

    }//GEN-LAST:event_btnGuardarActionPerformed

