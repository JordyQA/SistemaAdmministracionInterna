    public void accionClic() {
        int fila = tblProductos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "PORFAVOR SELECCIONE UNA FILA DE LA TABLA");
        } else {
            String cod = tblProductos.getValueAt(fila, 0).toString();
            String des = tblProductos.getValueAt(fila, 1).toString();
            int cant = Integer.parseInt(tblProductos.getValueAt(fila, 2).toString());
            float pre = Float.parseFloat(tblProductos.getValueAt(fila, 3).toString());
            String cat = tblProductos.getValueAt(fila, 4).toString();
            Date freg = Date.valueOf(tblProductos.getValueAt(fila, 5).toString());

            //ESTA PARTE ES PARA PODER MOSTRAR EN LAS CAJAS DE TEXTO
            txtCodigo.setText(cod);
            txtDescripcion.setText(des);
            txtCantidad.setText("" + cant);
            txtPrecio.setText("" + pre);
            txtCategoria.setText(cat);
            feRegistro.setDate(freg);
        }
    }

    //creamos un metodo actualizar 
    public void Actualizar() {
        String cod = txtCodigo.getText();
        String des = txtDescripcion.getText();
        int cant = Integer.parseInt(txtCantidad.getText());
        float pre = Float.parseFloat(txtPrecio.getText());
        String cat = txtCategoria.getText();
        Calendar cal;
        int d, m, a;
        cal = feRegistro.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;

        stock.setCodigo(cod);
        stock.setDescripcion(des);
        stock.setCantidad(cant);
        stock.setPrecio(pre);
        stock.setCategoria(cat);
        stock.setFechaRegistro(new Date(a, m, d));

        sc.actualizar(stock);
    }


